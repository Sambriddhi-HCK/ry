age = int(input('Enter your age (in number): '))
if (age>=0 and age<=10):
        print('You are in age group 0-10')
elif(age>=11 and age<=20):
    print('You are in age group 11-20')
elif(age>=21 and age<=30):
    print('You are in age group 21-30')
elif(age>=31 and age<=40):
    print('You are in age group 31-40')
elif(age>=41 and age<=50):
    print('You are in age group 41-50')
elif(age>=51 and age<=60):
    print('You are in age group 51-60')
else:
    print('You are in age group 60+')
print('The age checking program is over')
